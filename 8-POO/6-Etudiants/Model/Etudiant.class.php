<?php
include_once "../Model/Filiere.class.php";

class Etudiant{
    private string $nom;
    private string $prenom;
    private string $dateNaiss;
    private static int $id;
    private Filiere $filiere;

public function __construct(string $nom,string $prenom,string $dateNaiss,string $libelle){
    $this->nom = $nom;
    $this->prenom = $prenom;
    $this->dateNaiss = $dateNaiss;      
    $this->filiere = new Filiere($libelle); 
}

public function __toString(){
    $string = "Je suis l'étudiant $this->nom $this->prenom ma date de naissance est : $this->dateNaiss.";
    return $string;
}







    /**
     * Get the value of nom
     */ 
    public function getNom()
    {
        return $this->nom;
    }

    /**
     * Set the value of nom
     *
     * @return  self
     */ 
    public function setNom($nom)
    {
        $this->nom = $nom;

        return $this;
    }

    /**
     * Get the value of prenom
     */ 
    public function getPrenom()
    {
        return $this->prenom;
    }

    /**
     * Set the value of prenom
     *
     * @return  self
     */ 
    public function setPrenom($prenom)
